<p style="text-align: justify;">Công ty TNHH Hãng Kiểm toán AASC (Hãng Kiểm toán AASC), phát triển và trưởng thành từ Công ty Dịch vụ Tư vấn Tài chính Kế toán và Kiểm toán Bộ Tài chính - Thành viên HLB Quốc tế tại Việt Nam (Mạng lưới Kế toán và Tư vấn toàn cầu). Hãng Kiểm toán AASC là một trong hai tổ chức hợp pháp đầu tiên và lớn nhất của Việt Nam hoạt động trong lĩnh vực Kiểm toán, Tư vấn tài chính, Kế toán, Thuế, Thẩm định giá và các dịch vụ khác được pháp luật cho phép. Hiện nay, AASC có trụ sở chính đặt tại Hà Nội, chi nhánh tại TP Hồ Chí Minh và chi nhánh tại Quảng Ninh.</p>
<p style="text-align: justify;">Theo nhận xét của Giáo sư - Tiến sỹ Vương Đình Huệ nguyên Phó Thủ tướng Chính phủ; xếp hạng của Bộ Tài chính; Hội Kiểm toán viên hành nghề Việt Nam: AASC hiện nay là Nhà cung cấp dịch vụ lớn “Đầu đàn của Hệ thống các Công ty Kiểm toán Việt Nam”, với khách hàng đa dạng và có dịch vụ kiểm toán Báo cáo quyết toán dự án hoàn thành, dịch vụ xác định giá trị doanh nghiệp xếp hàng nhất của Ngành Kiểm toán độc lập Việt Nam.</p>
<p style="text-align: justify;">Cùng với các Công ty kiểm toán Big 4 đang hoạt động tại Việt Nam, AASC là một trong 5 Doanh nghiệp Kiểm toán năng động có doanh thu hàng năm, hệ thống khách hàng, số Kiểm toán viên và nhân viên lớn nhất hiện nay (trên 100 Kiểm toán viên Việt Nam, 06 Hội viên ACCA, 03 Hội viên CPA Úc, 02 Hội viên CIMA, 01 Hội viên CIA, 34 Thẩm định viên về giá, 36 Chứng chỉ Tư vấn Thủ tục về Thuế và gần 500 nhân viên). Trong gần 30 năm hoạt động, dù dưới hình thức DNNN hay công ty TNHH, tôn chỉ hoạt động của AASC vẫn luôn là cung cấp những dịch vụ có chất lượng cao nhất vì lợi ích hợp pháp của khách hàng. Khách hàng của chúng tôi bao gồm tất cả các lĩnh vực kinh tế, thành phần kinh tế: Tập đoàn kinh tế, Tổng công ty nhà nước, Công ty có vốn đầu tư nước ngoài, Công ty niêm yết, Công ty cổ phần, Ngân hàng thương mại, các Dự án có sử dụng vốn ODA, vốn viện trợ của Ngân hàng Thế giới (WB), Ngân hàng Phát triển Châu Á (ADB) cũng như các tổ chức tín dụng quốc tế khác và các công trình đầu tư xây dựng cơ bản trong nước và nước ngoài đầu tư vào Việt Nam và đầu tư tại các nước trong khu vực.</p>
